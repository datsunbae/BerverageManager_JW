    private void LoadTableData(){ 
        
        defaultTableModel.setRowCount(0);
        List<NhanVien_DTO> listNV = staff_BUS.getAllStaff();
        for(NhanVien_DTO nv : listNV){
            defaultTableModel.addRow(new Object[] {nv.getMaNV(), nv.getTenNV(), nv.getSdtNV(), nv.getDiaChiNV(), nv.getEmailNV()});
        }
    }
    
    private void EventSelectRow(){
        ListSelectionModel listSelectionModel = JTable_Staff.getSelectionModel();
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int row = JTable_Staff.getSelectedRow();
                
                if(row >= 0){
                    String maNV = String.valueOf(JTable_Staff.getValueAt(row, 0));
                    String tenNV = String.valueOf(JTable_Staff.getValueAt(row, 1));
                    String sdtNV = String.valueOf(JTable_Staff.getValueAt(row, 2));
                    String diaChiNV = String.valueOf(JTable_Staff.getValueAt(row, 3));
                    String emailNV = String.valueOf(JTable_Staff.getValueAt(row, 4));

                    JTF_MaNV.setText(maNV);
                    JTF_TenNV.setText(tenNV);
                    JTF_SDTNV.setText(sdtNV);
                    JTF_DiaChiNV.setText(diaChiNV);
                    JTF_EmailNV.setText(emailNV);

                    JTF_MaNV.setEnabled(false);
                    JBtn_AddStaff.setEnabled(false);
                    
                }
           }
        });
    }
    
