    private NhanVien_DTO getDataStaffFromGUI(){
        
        String maNV = JTF_MaNV.getText();
        String tenNV = JTF_TenNV.getText();
        String sdtNV = JTF_SDTNV.getText();
        String diaChiNV = JTF_DiaChiNV.getText();
        String emailNV = JTF_EmailNV.getText();
        
        NhanVien_DTO nv = new NhanVien_DTO();
        nv.setMaNV(maNV);
        nv.setTenNV(tenNV);
        nv.setSdtNV(sdtNV);
        nv.setDiaChiNV(diaChiNV);
        nv.setEmailNV(emailNV);
        
        return nv;
    }
    
    private void JBtn_AddStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtn_AddStaffActionPerformed
        
        if(JTF_MaNV.getText().compareTo("") != 0 && JTF_TenNV.getText().compareTo("") != 0){
            staff_BUS.AddStaff(getDataStaffFromGUI());
            LoadTableData();
            JOptionPane.showMessageDialog(this, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            ClearTextFiled();
        }
        else{
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin nhân viên", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_JBtn_AddStaffActionPerformed

