    private void JBTN_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTN_ThemActionPerformed
        
        //kiem tra so luong hop le
        int rowSelected = JTB_SP.getSelectedRow();
        if(rowSelected >= 0){
            String maSP = String.valueOf(JTB_SP.getValueAt(rowSelected, 0));
            SanPham_DTO sp = getProductByMaSP(maSP);
            int slTonKho = Integer.parseInt(String.valueOf(JTB_SP.getValueAt(rowSelected, 2)));
            int slDVT_TFSL = Integer.parseInt(JTF_SL.getText());
            int slDVT_TBSP = Integer.parseInt(String.valueOf(JTB_SP.getValueAt(rowSelected, 2)));
            if(checkDVTC_DVTP(String.valueOf(JCB_DVT.getSelectedItem()))){//dvtchinh
                int slQuyDoi = getProductByMaSP(maSP).getQuiDoi();
                int slBan  = slQuyDoi * slDVT_TFSL;

                if(slBan <= slTonKho){
                    //cap nhat sl table SP - GUI
                    int slDVT_TBSP_CapNhat = slDVT_TBSP - slBan;
                    JTB_SP.setValueAt(slDVT_TBSP_CapNhat, rowSelected, 2);
                    String sltQuyDoi_TBSP_CapNhat = tinhTonKhoQuyDoi(sp.getDvtChinh(), sp.getDvtPhu(), slDVT_TBSP_CapNhat, sp.getQuiDoi());
                    JTB_SP.setValueAt(sltQuyDoi_TBSP_CapNhat, rowSelected, 3);
                    addSPVaoTB_CTHD(rowSelected, maSP); //add sp vao tb_CTHD
                }
                else{
                    JOptionPane.showMessageDialog(this, "Số lượng tồn kho không đủ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } 
            }
            else{
                if(slDVT_TFSL <= slTonKho){
                    //cap nhat sl table SP - GUI
                    int slDVT_TBSP_CapNhat = slDVT_TBSP - slDVT_TFSL;
                    JTB_SP.setValueAt(slDVT_TBSP_CapNhat, rowSelected, 2);
                    String sltQuyDoi_TBSP_CapNhat = tinhTonKhoQuyDoi(sp.getDvtChinh(), sp.getDvtPhu(), slDVT_TBSP_CapNhat, sp.getQuiDoi());
                    JTB_SP.setValueAt(sltQuyDoi_TBSP_CapNhat, rowSelected, 3);
                    addSPVaoTB_CTHD(rowSelected, maSP); //add sp vao tb_CTHD
                }
                else{
                    JOptionPane.showMessageDialog(this, "Số lượng tồn kho không đủ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } 
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn sản phẩm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_JBTN_ThemActionPerformed

