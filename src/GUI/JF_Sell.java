    private void JBTN_XacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTN_XacNhanActionPerformed
        
        int tongDongTBHD = JTB_HD.getRowCount();
        if(tongDongTBHD > 0){
            String ngayLap = dateFormatSQL.format(JDC_Date.getDate());
            NhanVien_DTO nv = (NhanVien_DTO) JCB_NV.getSelectedItem();
            String maNV = nv.getMaNV();
            KhachHang_DTO kh = (KhachHang_DTO) JCB_KH.getSelectedItem();
            String maKH = kh.getMaKH();


            float tongTien = 0;
            for(int i=0; i < tongDongTBHD; i++){
                float thanhTien = Float.parseFloat(String.valueOf(JTB_HD.getValueAt(i, 5)));
                tongTien += thanhTien;
            }

            //tao don hang
            DonHang_DTO dh = new DonHang_DTO();
            dh.setNgayLap(ngayLap);
            dh.setMaNV(maNV);
            dh.setMaKH(maKH);
            dh.setTongTien(tongTien);
            dh_BUS.addDonHang(dh);
            
            int maDH = dh_BUS.getMaDHVuaThem();
            JTF_MaHD.setText("MDH" + String.valueOf(maDH));
            JTF_TongTien.setText(String.format("%.0f", tongTien));
            
            //tao ct don hang
            for(int i = 0; i < tongDongTBHD; i++){
                CT_DonHang_DTO ctdh = new CT_DonHang_DTO();
                ctdh.setMaDH(maDH);
                ctdh.setMaSP(String.valueOf(JTB_HD.getValueAt(i, 0)));
                ctdh.setSoLuong(Integer.parseInt(String.valueOf(JTB_HD.getValueAt(i, 4))));
                String maDVT = getDonViByTenDV_SP(String.valueOf(JTB_HD.getValueAt(i, 3))).getMaDV();
                ctdh.setMaDVT(maDVT);
                ctdh.setThanhTien(Float.parseFloat(String.valueOf(JTB_HD.getValueAt(i, 5))));
                ctDH_BUS.addCTDonHang(ctdh);
            }
            
            JBTN_Them.setEnabled(false);
            JBTN_XoaSP.setEnabled(false);
            JBTN_XacNhan.setEnabled(false);
            JBTN_TaoHDMoi.setEnabled(false);
        }
        else{
            JOptionPane.showMessageDialog(this, "Không thể tạo đơn hàng", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_JBTN_XacNhanActionPerformed

    private void JBTN_ThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTN_ThanhToanActionPerformed
        //xu ly ton kho
        int slDVT;
        int slBan;
        int tongSLTKCapNhat;
        int tongDongCTHD = JTB_HD.getRowCount();
        for(int i = 0; i < tongDongCTHD; i++){
            String maSP = String.valueOf(JTB_HD.getValueAt(i, 0));
            String tenDVT = String.valueOf(JTB_HD.getValueAt(i, 3));
            SanPham_DTO sp = getProductByMaSP(maSP);
            TonKho_DTO tk = getTonKhoByMaSP(maSP);
            if(checkDVTC_DVTP(tenDVT)){ // dvt chinh
                slDVT = Integer.parseInt(String.valueOf(JTB_HD.getValueAt(i, 4)));
                slBan = slDVT * sp.getQuiDoi();
                tongSLTKCapNhat = tk.getSlTon() - slBan;
                String slTKQuyDoi = tinhTonKhoQuyDoi(sp.getDvtChinh(), sp.getDvtPhu(), tongSLTKCapNhat, sp.getQuiDoi());
                tk.setSlTon(tongSLTKCapNhat);
                tk.setSlTonQuiDoi(slTKQuyDoi);
                tonKho_BUS.UpdateTonKho(tk);
            }
            else {
                slDVT = Integer.parseInt(String.valueOf(JTB_HD.getValueAt(i, 4)));
                tongSLTKCapNhat = tk.getSlTon() - slDVT;
                String slTKQuyDoi = tinhTonKhoQuyDoi(sp.getDvtChinh(), sp.getDvtPhu(), tongSLTKCapNhat, sp.getQuiDoi());
                tk.setSlTon(tongSLTKCapNhat);
                tk.setSlTonQuiDoi(slTKQuyDoi);
                tonKho_BUS.UpdateTonKho(tk);
            }
        }
        
        //load tb sanpham
        JOptionPane.showMessageDialog(this, "Thanh toán thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        LoadTableData(tonKho_BUS.getAllTonKho());
        RefreshJFrame();
    }//GEN-LAST:event_JBTN_ThanhToanActionPerformed

