    private void EventSelectRow(){
        ListSelectionModel listSelectionModel = JTB_SP.getSelectionModel();
        listSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); 
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()){
                    int row = JTB_SP.getSelectedRow();
                    if(row >= 0){
                        String maSP = String.valueOf(JTB_SP.getValueAt(row, 0));
                        String tenSP = String.valueOf(JTB_SP.getValueAt(row, 1));
                        SanPham_DTO spSelected = getProductByMaSP(maSP);
                        JTF_TenSP.setText(tenSP);
                        
                        //load DVT
                        LoadSuccessCB_DVT = false;
                        JCB_DVT.removeAllItems();
                        String dvtc = spSelected.getDvtChinh();
                        String dvtp = spSelected.getDvtPhu();
                        List<DonVi_DTO> listDVT = getListDVTByMaDVT_SP(dvtc, dvtp);
                        for(DonVi_DTO dvt : listDVT){
                            
                            JCB_DVT.addItem(dvt);
                        }
                        
                        LoadSuccessCB_DVT = true;
                        // si, le
                        DonVi_DTO dvt = (DonVi_DTO) JCB_DVT.getSelectedItem();
                        eventGiaBanSiLe(row, spSelected);
                    }
                }
            }
        });
    }
    
