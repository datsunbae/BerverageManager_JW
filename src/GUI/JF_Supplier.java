     private void LoadTableData(){ 
        
        defaultTableModel.setRowCount(0);
        List<NhaCungCap_DTO> listNCC = supplier_BUS.getAllSupplier();
        for(NhaCungCap_DTO ncc : listNCC){
            defaultTableModel.addRow(new Object[] {ncc.getMaNCC(), ncc.getTenNCC(), ncc.getSdtNCC(), ncc.getDiaChiNCC(), ncc.getEmailNCC()});
        }
    }
     
    private void EventSelectRow(){
        ListSelectionModel listSelectionModel = JTB_Supplier.getSelectionModel();
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int row = JTB_Supplier.getSelectedRow();
                
                if(row >= 0){
                    String maNCC = String.valueOf(JTB_Supplier.getValueAt(row, 0));
                    String tenNCC = String.valueOf(JTB_Supplier.getValueAt(row, 1));
                    String sdtNCC = String.valueOf(JTB_Supplier.getValueAt(row, 2));
                    String diaChiNCC = String.valueOf(JTB_Supplier.getValueAt(row, 3));
                    String emailNCC = String.valueOf(JTB_Supplier.getValueAt(row, 4));

                    JTF_MaNCC.setText(maNCC);
                    JTF_TenNCC.setText(tenNCC);
                    JTF_SDTNCC.setText(sdtNCC);
                    JTF_DiaChiNCC.setText(diaChiNCC);
                    JTF_EmailNCC.setText(emailNCC);

                    JTF_MaNCC.setEnabled(false);
                    JBtn_AddSupplier.setEnabled(false);
                }
            }
        });
    }
    
    private void ClearTextFiled(){
        JTF_MaNCC.setText("");
        JTF_TenNCC.setText("");
        JTF_SDTNCC.setText("");
        JTF_DiaChiNCC.setText("");
        JTF_EmailNCC.setText("");
    }
    
    private NhaCungCap_DTO getDataSupplierFromGUI(){
        
        String maNCC = JTF_MaNCC.getText();
        String tenNCC = JTF_TenNCC.getText();
        String sdtNCC = JTF_SDTNCC.getText();
        String diaChiNCC = JTF_DiaChiNCC.getText();
        String emailNCC = JTF_EmailNCC.getText();
        
        NhaCungCap_DTO ncc = new NhaCungCap_DTO();
        ncc.setMaNCC(maNCC);
        ncc.setTenNCC(tenNCC);
        ncc.setSdtNCC(sdtNCC);
        ncc.setDiaChiNCC(diaChiNCC);
        ncc.setEmailNCC(emailNCC);
        
        return ncc;
    }
     
