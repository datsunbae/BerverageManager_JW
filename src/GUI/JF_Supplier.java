    private void JBtn_DeleteSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtn_DeleteSupplierActionPerformed
        int row = JTB_Supplier.getSelectedRow();
        
        if(row >= 0){
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?");
        
            if(confirm == JOptionPane.YES_OPTION){
                String maNCC = JTF_MaNCC.getText();
                supplier_BUS.DeleteSupplier(maNCC);
                
                LoadTableData();
                JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                ClearTextFiled();  
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn nhà cung cấp cần xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_JBtn_DeleteSupplierActionPerformed

    private void JBtn_SearchSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JBtn_SearchSupplierMouseClicked
        if(JTF_SearchSupplier.getText().length() > 0){
            String tenNCC = JTF_SearchSupplier.getText();
 
            defaultTableModel.setRowCount(0);
            List<NhaCungCap_DTO> listNCC = supplier_BUS.SearchSupplier(tenNCC);
            for(NhaCungCap_DTO ncc : listNCC){
                defaultTableModel.addRow(new Object[] {ncc.getMaNCC(), ncc.getTenNCC(), ncc.getSdtNCC(), ncc.getDiaChiNCC(), ncc.getEmailNCC()});
            }
        }
        else{
            LoadTableData();
        }
    }//GEN-LAST:event_JBtn_SearchSupplierMouseClicked

