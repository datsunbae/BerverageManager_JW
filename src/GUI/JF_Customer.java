    private void JBtn_BackHomeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JBtn_BackHomeMouseClicked
        if(JTF_SearchCustomer.getText().length() > 0){
            String tenKH = JTF_SearchCustomer.getText();
 
            defaultTableModel.setRowCount(0);
            List<KhachHang_DTO> listKH = customer_BUS.SearchCustomer(tenKH);
            for(KhachHang_DTO kh : listKH){
                defaultTableModel.addRow(new Object[] {kh.getMaKH(), kh.getTenKH(), kh.getSdtKH(), kh.getDiaChiKH(), kh.getEmailKH()});
            }
        }
        else{
            LoadTableData();
        }
    }//GEN-LAST:event_JBtn_BackHomeMouseClicked

