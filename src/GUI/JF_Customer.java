    private void LoadTableData(){ 
        
        defaultTableModel.setRowCount(0);
        List<KhachHang_DTO> listKH = customer_BUS.getAllCustomer();
        for(KhachHang_DTO kh : listKH){
            defaultTableModel.addRow(new Object[] {kh.getMaKH(), kh.getTenKH(), kh.getSdtKH(), kh.getDiaChiKH(), kh.getEmailKH()});
        }
    }
    
    private void EventSelectRow(){
        ListSelectionModel listSelectionModel = JTB_Customer.getSelectionModel();
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int row = JTB_Customer.getSelectedRow();
                
                if(row >= 0){
                    String maKH = String.valueOf(JTB_Customer.getValueAt(row, 0));
                    String tenKH = String.valueOf(JTB_Customer.getValueAt(row, 1));
                    String sdtKH = String.valueOf(JTB_Customer.getValueAt(row, 2));
                    String diaChiKH = String.valueOf(JTB_Customer.getValueAt(row, 3));
                    String emailKH = String.valueOf(JTB_Customer.getValueAt(row, 4));

                    JTF_MaKH.setText(maKH);
                    JTF_TenKH.setText(tenKH);
                    JTF_SDTKH.setText(sdtKH);
                    JTF_DiaChiKh.setText(diaChiKH);
                    JTF_EmailKH.setText(emailKH);

                    JTF_MaKH.setEnabled(false);
                    JBtn_AddCustomer.setEnabled(false);
                    
                }
           }
        });
    }
    
    private void ClearTextFiled(){
        JTF_MaKH.setText("");
        JTF_TenKH.setText("");
        JTF_SDTKH.setText("");
        JTF_DiaChiKh.setText("");
        JTF_EmailKH.setText("");
    }
    
    private KhachHang_DTO getDataCustomerFromGUI(){
        
        String maKH = JTF_MaKH.getText();
        String tenKH = JTF_TenKH.getText();
        String sdtKH = JTF_SDTKH.getText();
        String diaChiKH = JTF_DiaChiKh.getText();
        String emailKH = JTF_EmailKH.getText();
        
        KhachHang_DTO kh = new KhachHang_DTO();
        kh.setMaKH(maKH);
        kh.setTenKH(tenKH);
        kh.setSdtKH(sdtKH);
        kh.setDiaChiKH(diaChiKH);
        kh.setEmailKH(emailKH);
        
        return kh;
    }
    
