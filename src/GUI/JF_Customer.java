    private void JBtn_DeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtn_DeleteCustomerActionPerformed
        int row = JTB_Customer.getSelectedRow();
        
        if(row >= 0){
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?");
        
            if(confirm == JOptionPane.YES_OPTION){
                String maKH = JTF_MaKH.getText();
                customer_BUS.DeleteCustomer(maKH);
                
                LoadTableData();
                JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                ClearTextFiled();  
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn khách hàng cần xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_JBtn_DeleteCustomerActionPerformed

    private void JBtn_AddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtn_AddCustomerActionPerformed
         
        if(JTF_MaKH.getText().compareTo("") != 0 && JTF_TenKH.getText().compareTo("") != 0){
            customer_BUS.AddCustomer(getDataCustomerFromGUI());
            LoadTableData();
            JOptionPane.showMessageDialog(this, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            ClearTextFiled();
        }
        else{
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin khách hàng", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_JBtn_AddCustomerActionPerformed

